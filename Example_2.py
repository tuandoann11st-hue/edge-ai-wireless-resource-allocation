# Throughput calculation
# ================================

def compute_sum_rate(power, channel_gain):

    N = len(power)

    sum_rate = 0

    for i in range(N):

        signal = power[i] * channel_gain[i,i]

        interference = 0

        for j in range(N):

            if j != i:
                interference += power[j] * channel_gain[j,i]

        sinr = signal / (interference + NOISE)

        rate = np.log2(1 + sinr)

        sum_rate += rate

    return sum_rate

