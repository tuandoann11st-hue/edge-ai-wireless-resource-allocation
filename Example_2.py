import torch
import torch.nn as nn
import torch.optim as optim
import numpy as np
import matplotlib.pyplot as plt

# ----------------------------
# Parameters
# ----------------------------

num_users = 10
epochs = 500
power_budget = 10
noise = 1e-2

# ----------------------------
# Generate user positions
# ----------------------------

positions = torch.rand(num_users, 2)  # (x,y)

# Distance-based adjacency
dist_matrix = torch.cdist(positions, positions)

adj = torch.exp(-dist_matrix)  # closer → stronger interference
adj.fill_diagonal_(0)

# ----------------------------
# GNN Model (improved)
# ----------------------------

class GNN(nn.Module):
    def __init__(self):
        super(GNN, self).__init__()

        self.fc1 = nn.Linear(1, 32)
        self.fc2 = nn.Linear(32, 32)
        self.fc3 = nn.Linear(32, 1)

    def forward(self, x, adj):

        # Message passing
        agg = torch.matmul(adj, x)

        h = torch.relu(self.fc1(agg))
        h = torch.relu(self.fc2(h))
        out = self.fc3(h)

        # Softmax → allocation ratio
        alloc = torch.softmax(out.squeeze(), dim=0)

        return alloc

model = GNN()
optimizer = optim.Adam(model.parameters(), lr=0.005)

# ----------------------------
# Training
# ----------------------------

throughputs = []

for epoch in range(epochs):

    # 🔥 Random channel mỗi epoch
    channel_gains = torch.rand(num_users)

    x = channel_gains.view(-1,1)

    # Forward
    alloc = model(x, adj)

    # Power allocation
    power = alloc * power_budget

    # 🔥 SINR chuẩn (có interference)
    interference = torch.matmul(adj, (power * channel_gains))
    signal = power * channel_gains

    sinr = signal / (interference + noise)

    # Throughput
    throughput = torch.sum(torch.log2(1 + sinr))

    # Loss
    loss = -throughput

    # Backprop
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()

    throughputs.append(throughput.item())

    if epoch % 50 == 0:
        print(f"Epoch {epoch} | Throughput: {throughput.item():.3f}")

# ----------------------------
# Plot (IEEE style)
# ----------------------------

throughputs = np.array(throughputs)

# Moving average
window = 20
smooth = np.convolve(throughputs, np.ones(window)/window, mode='valid')

plt.figure(figsize=(10,5))

plt.plot(throughputs, alpha=0.4, label="Raw Throughput")
plt.plot(range(window-1, len(throughputs)), smooth, linewidth=2, label="Smoothed Throughput")

plt.xlabel("Epoch")
plt.ylabel("Network Throughput")
plt.title("Improved Edge AI GNN Wireless Resource Allocation")
