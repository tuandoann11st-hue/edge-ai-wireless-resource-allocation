from torch_geometric.nn import GCNConv
import numpy as np
import matplotlib.pyplot as plt

# ================================
# Simulation parameters
# ================================

NUM_USERS = 10
AREA_SIZE = 100
NOISE = 1e-9
MAX_POWER = 1.0

# ================================
# Generate wireless network
# ================================

def generate_network(num_users):

    positions = np.random.rand(num_users,2) * AREA_SIZE

    channel_gain = np.zeros((num_users,num_users))

    for i in range(num_users):
        for j in range(num_users):

            dist = np.linalg.norm(positions[i] - positions[j]) + 1e-3

            pathloss = 1 / (dist**2)

            fading = np.random.rayleigh()

            channel_gain[i,j] = pathloss * fading

    return positions, channel_gain

# ================================
# Graph construction
# ================================

def build_graph(channel_gain):

    edge_index = []
    edge_weight = []

    N = channel_gain.shape[0]

    for i in range(N):
        for j in range(N):
            if i != j:

                edge_index.append([i,j])
                edge_weight.append(channel_gain[i,j])

    edge_index = torch.tensor(edge_index).t().contiguous()
    edge_weight = torch.tensor(edge_weight, dtype=torch.float)

    return edge_index, edge_weight

# ================================
# Node features
# ================================

def build_node_features(channel_gain):

    N = channel_gain.shape[0]

    features = []

    for i in range(N):

        gain = channel_gain[i,i]

        interference = np.sum(channel_gain[:,i]) - gain

        demand = np.random.rand()

        features.append([gain, interference, demand])

    return torch.tensor(features, dtype=torch.float)

# ================================
# GNN Model
